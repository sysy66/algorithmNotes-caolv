class Solution:
    def findRotation(self, mat: list[list[int]], target: list[list[int]]) -> bool:
        def tans90(matrix):
            matrix = matrix[::-1]
            res = []
            for line in zip(*matrix):
                res.append(list(line))
            return res

        if mat == target:
            return True
        for _ in range(3):
            mat = tans90(mat)
            if mat == target:
                return True

        return False